<h2>Big Math Flash Cards</h2>

Flip the card just like a real flash card! Simple, clean and easy to use interface. The front side of the card shows the question and the back side of the card shows the possible answers. Just tap to flip the card. Once you answer the question correctly, flick the card off the screen by swiping up.
<br><br>
<h3>Features</h3>
<ul>
<li>Addition, Subtraction, Multiplication, Division
<li>Randomly generated numbers
<li>Score percentage and elapsed time displayed
<li>Configurable number of questions and difficulty levels
<li>Other features available via "In App Purchase"
</ul>
<br>
<h3>Paid Features</h3>
<ul>
<li>Review Incorrect Answers - review of questions that were answered incorrectly
<li>View Past Score History - viewing of past scores in a easy to read card format
<li>Practice Specific Numbers - practicing of a specific number
<li>Advanced Difficulty Levels - difficulty levels 4, 5 and 6
<li>Mixed Operation Flash Cards - ability to have cards with mixed operations
</ul>
